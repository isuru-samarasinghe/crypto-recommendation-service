package com.xm.cryptoservice.config;

/**
 * Constants class to hold application-wide constant values.
 * This class is final and cannot be extended.
 */
public final class Constants {
    /**
     * DECIMAL_PRECISION is the precision for decimal numbers used in the
     * application.
     * It defines the total number of digits in a number.
     */
    public static final int DECIMAL_PRECISION = 20;

    /**
     * DECIMAL_SCALE is the scale for decimal numbers used in the application.
     * It defines the number of digits to the right of the decimal point.
     */
    public static final int DECIMAL_SCALE = 15;
}